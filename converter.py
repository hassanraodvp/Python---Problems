# Kilometers to Miles Converter 
num = float(input("Enter a number in kilometers: "))

miles = num * 0.621371;
print(f"{num} kilometers is equal to {miles} miles.")

# Miles to Kilometers Converter 
number = float(input("Enter a number in miles: "))

kilometers = number * 1.609344
print(f"{number} miles is equal to {kilometers} kilometers.")